public class Cliente {
    public String nome;
    public String cpf;
    public String rg;
    public String endereco;
    public String email;

    public Cliente(String nome, String cpf, String rg, String endereco, String email) {
        this.nome = nome;
        this.cpf = cpf;
        this.rg = rg;
        this.endereco = endereco;
        this.email = email;
    }
}
